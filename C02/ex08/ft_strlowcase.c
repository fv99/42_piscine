#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (*(str + i))
	{
		i++;
	}
	write(1, str, i);
}

char	*ft_strlowcase(char *str)
{
	int i;

	i = 0; 
	while(str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
			i++;
		}
		else
			i++;
	}
	return (str);
}

int	main(void)
{
	char r[] = {"FuCk YoU"};

	ft_strlowcase(r);
	ft_putstr(r);
	
	return (0);
}